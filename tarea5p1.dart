void main() {
  int x = 15;
  int y = 20;
  print('x=15, y=20');
  print('x == y?: ${x == y}');
  print('x != y?: ${x != y}');
  print('x > y?: ${x > y}');
  print('x < y?: ${x < y}');
  print('x >= y?: ${x >= y}');
  print('x <= y?: ${x <= y}');
  print('(x > 10) && (y > 10)?: ${(x > 10) && (y > 10)}');
  print('(x > 20) || (y > 20)?: ${(x > 20) || (y > 20)}');
  print('!(x == y)?: ${!(x == y)}');
}
