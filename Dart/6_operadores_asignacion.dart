void main() {
  //Operadores de Asignación
  int numero = 17;
  print(numero);

  numero += 5;    // numero = numero + 5  → numero = 15
  print(numero);
  numero -= 3;    // numero = numero - 3  → numero = 12
  print(numero);
  numero *= 2;    // numero = numero * 2  → numero = 24
  print(numero);
  numero ~/= 4;   // numero = numero ~/ 4 → numero = 6
  print(numero);


}