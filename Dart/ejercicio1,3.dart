void main() {
  /*
        Ejercicio 1.3: Operaciones Matemáticas Básicas
        Descripción: Crea un programa que realice operaciones básicas con dos números.
        Requisitos:
        Declara dos variables numéricas: a = 15, b = 4
        Calcula y imprime: suma, resta, multiplicación, división, división entera y módulo
        Cada resultado debe tener una etiqueta
   */
  int a = 15;
  int b = 4;

  print(a + b); // Suma: 19
  print(a - b); // Resta: 11
  print(a * b); // Multiplicación: 60
  print(a / b); // División: 3.75
  print(a ~/ b); // División entera: 3
  print(a % b); // Módulo: 3
}
