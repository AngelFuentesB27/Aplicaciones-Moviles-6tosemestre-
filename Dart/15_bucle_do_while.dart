void main() {
  
  //Do While
  print('\nCiclo Do While Incremento');
  int numero = 1;
  do {
    print('Número: $numero');
    numero += 2;
  } while (numero <= 5);
  // Salida: Número: 1, 2, 3
  
  //Do While Incrementos Diferentes
  print('\nCiclo Do While Incremento');
  numero = 0;
  do {
    print('Número: $numero');
    numero += 2;
  } while (numero <= 5);
}